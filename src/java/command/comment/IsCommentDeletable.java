package command.comment;

import javax.servlet.http.HttpSession;

/**
 *
 * @author Siriquelle
 */
public class IsCommentDeletable {
    //<editor-fold defaultstate="collapsed" desc="isCommentDeletable">
    public static String run(int commentId, HttpSession session) {
        String delete = "";

        if (IsUsersComment.run(commentId, session))
          {
            delete = "<a class=\"delete_comment\" href=\"/DeleteComment?commentId=" + commentId + "\" > [delete] </a>";
          }

        return delete;
    }// </editor-fold>
}
