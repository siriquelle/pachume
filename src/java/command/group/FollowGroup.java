package command.group;

import model.DataBaseConnection;

/**
 *
 * @author Siriquelle
 */
public class FollowGroup {
    //<editor-fold defaultstate="collapsed" desc="joinGroup">

    public static boolean run(int userId, String groupId) {

        boolean isChanelJoined = true;

        try
          {

            String groupName = GetGroupName.run(groupId);

            String joinGroup_sql = "INSERT INTO groupsmember(userId, groupId, groupName)" +
                    " VALUES(" + userId + ", '" + groupId + "', '" + groupName + "');";

            DataBaseConnection joinGroup_db = new DataBaseConnection();
            joinGroup_db.connect();
            joinGroup_db.execUpdate(joinGroup_sql);
            joinGroup_db.close();

          } catch (Exception e)
          {
            isChanelJoined = false;
          }

        return isChanelJoined;

    }
// </editor-fold>
}
