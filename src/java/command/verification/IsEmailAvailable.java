package command.verification;

import java.sql.ResultSet;
import model.DataBaseConnection;

/**
 *
 * @author Siriquelle
 */
public class IsEmailAvailable {
    //<editor-fold defaultstate="collapsed" desc="isEmailAvailable">

    public static boolean run(String email) {

        boolean isEmailAvailable = false;

        try
          {
            DataBaseConnection isEmailAvailable_db = new DataBaseConnection();
            isEmailAvailable_db.connect();
            String isEmailAvailable_sql = "SELECT email FROM user WHERE email = '" + email.trim() + "';";
            ResultSet isEmailAvailable_rs = isEmailAvailable_db.execSQL(isEmailAvailable_sql);

            if (isEmailAvailable_rs.next())
              {
                isEmailAvailable = false;
              } else
              {
                isEmailAvailable = true;
              }
            isEmailAvailable_db.close();
          } catch (Exception e)
          {
            isEmailAvailable = false;
          }

        return isEmailAvailable;
    }
    // </editor-fold>
}
